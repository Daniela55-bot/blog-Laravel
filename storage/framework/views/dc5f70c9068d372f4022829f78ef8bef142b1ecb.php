                  <p>Mi generación de estudiantes ha establecido la tendencia de dispositivos personales, utilizando teléfonos inteligentes, tabletas y computadoras portátiles en casi todas nuestras tareas e interacciones cotidianas, tanto dentro como fuera del aula. Con el número de aplicaciones que se crean exponencialmente cada día, he observado a varios de mis amigos de la escuela secundaria y a sus hermanos para recopilar una lista de las diez mejores aplicaciones para los estudiantes:</p>
                  <p>Épic! ($ 7.99/mes dólares, acceso gratuito a los docentes)</p>
                  <p>¡Dirigido a los estudiantes de K-5, Epic! Ofrece una biblioteca -con más de 25,000 de los mejores libros para niños, es accesible -en cualquier momento y en cualquier dispositivo. Al descargar esta aplicación en sus dispositivos personales, los estudiantes siempre tendrán un libro a su alcance. La membresía mensual con acceso ilimitado a la biblioteca de Epic! cuesta menos que comprar un solo libro, y los estudiantes pueden obtener acceso libre a través de sus docentes y bibliotecarios.</p>
                  <p>Trello (GRATIS)</p>
                  <p>Los proyectos de grupo son una de las experiencias de aprendizaje más gratificantes y frustrantes. A veces puede parecer imposible delegar tareas a los miembros del grupo simultáneamente mientras se gestiona las fechas de vencimiento y los plazos del proyecto. Con Trello, puede realizar las metas de su proyecto grupal usando tablas, listas y tarjetas para asignar tareas a cada miembro del grupo, las cuales pueden marcar como completadas mientras progresa en su trabajo en grupo. Descargue esta aplicación gratuita para maximizar su productividad al trabajar en proyectos colaborativos.</p>
                  
                  <p>Duolingo (GRATIS)</p>
                  <p>Si alguna vez ha tratado de aprender un nuevo idioma, sabe que la repetición y la continuidad son dos de los factores más importantes para que este sea fluido. Para aquellos que buscan aprender un nuevo idioma, Duolingo iniciará con algo básico y llevará la fluidez básica a través de pruebas repetitivas y ejercicios. Para los aprendices de idiomas actuales, Duolingo llena las brechas entre las clases de idiomas -como el verano y el invierno- de esta manera puede mantener la continuidad e invertir menos tiempo en la revisión al principio de cada semestre. </p>
                  <p>Quizlet (GRATIS)</p>
                  <p>Quizlet es un método de estudio probado y verdadero para estudiantes de cualquier nivel. Esta aplicación es una forma sencilla y eficaz de crear conjuntos de información de estudio, el cual puede revisar de diferentes maneras: flashcards, concordancia o un juego de ritmo rápido llamado Gravity. Recientemente, Quizlet introdujo una nueva característica llamada Quizlet Learn, que tiene como objetivo iniciar su estudio con un plan adaptativo que muestra su progreso, le ayuda a mantenerse motivado con puntos de control alentadores y lo mantiene en el camino con útiles recordatorios de estudio.</p>
                  <a href="#!"><img class="img-fluid" src="imagen/imagen1.jpg" alt="..." /></a>
                  <p>Hopscotch (GRATIS)</p>
                  <p>La nueva "lengua extranjera", la codificación es la comunicación del futuro; Sin embargo, puede ser abrumador brincar a la codificación sin fondo en el tema. Hopscotch tiene como objetivo darle cierta experiencia con la codificación, lo que le permite experimentar con su lenguaje basado en bloques y darle la confianza para derivarse a lenguajes más complejos como Java y Python. Descargue esta aplicación gratuita si está buscando explorar el mundo de la codificación en su dispositivo personal sin tener que descargar aplicaciones complejas o aprender sintaxis difícil.</p>
                  <p>Mathway (GRATIS)</p>
                  <p>Mathway es simplemente un procesador matemático en línea que soluciona problemas de matemáticas -desde álgebra básica hasta cálculo avanzado. La mejor manera de usar esto como estudiante es revisar los problemas prácticos y analizar aquello sobre lo que no siente seguro, pero no pueden preguntarle a su docente en ese momento. De esta manera, usted puede estar seguro de que no está cometiendo los mismos errores una y otra vez a medida que trabaja a través de la práctica y los problemas de preparación para un examen.</p>
                  <p>30/30 (GRATIS)</p>
                  <p>Un administrador de productividad extrema, 30/30 le permite hacer una lista de tareas con tiempo de trabajo y períodos de descanso para mantenerlo en línea con su tarea y el estudio. Especialmente hacia el final del semestre, puede ser tan abrumador terminar la tarea, trabajar en proyectos y estudiar para los exámenes finales. Usando 30/30 puede ayudar a mantener su ansiedad tranquila, permitiéndole poner límite al tiempo que trabaja en cada tarea, y le da la posibilidad de tomar pausas planificadas entre las tareas.</p>
                  <p>Blackboard Mobile Learn (GRATIS con la suscripción de la escuela a Blackboard)</p>
                  <p>Aunque esta aplicación sólo es útil para los estudiantes de las escuelas que se suscriben a LMS de Blackboard, es extremadamente útil tenerlo en sus dispositivos portátiles. Al descargar esta aplicación, obtiene acceso inmediato a todas sus asignaciones de clase, calificaciones y archivos en cualquier lugar, lo que te permite ser productivo dondequiera que esté.</p>
                  <p>EasyBib (GRATIS)</p>
                  <p>Aunque sea capaz de utilizar casi cualquier información que le guste para los proyectos de la escuela, siempre tiene que citar sus fuentes para evitar el plagio. EasyBib es una forma sencilla de hacer que sus trabajos citen la página. Escriba información individual sobre su fuente y EasyBib le pondrá el formato en que lo necesite.</p>
                  <p>Wolfram Alpha</p>
                  <p>WolframAlpha es el motor de búsqueda que supera a todos los motores de búsqueda. Este procesador computacional masivo toma su entrada y le da toda la información posible que considera que usted puede encontrar útil como respuesta. En lugar de ofrecerle enlaces como otros motores de búsqueda, WolframAlpha le da la respuesta directamente, por lo que es una manera eficiente de responder a sus preguntas. Lo mejor de todo, esta aplicación puede responder a casi cualquier pregunta o entrada que usted pone en ella, dede los problemas de matemáticas hasta las ecuaciones científicas.</p>
                  <p>Las aplicaciones son la clave para tomar el control de su productividad y su trabajo escolar. Como estudiante, he encontrado que el uso de aplicaciones ha ayudado a facilitar mi éxito en el aula. Al descargar estas aplicaciones en su dispositivo personal, siempre tendrá acceso inmediato a una amplia variedad de herramientas educativas que le ayudarán a progresar en su trabajo escolar y ser un estudiante exitoso.</p>
                  <p>Si disfrutó de esta lista de aplicaciones para los estudiantes, asegúrese de revisar el resto de información de esta serie de notas, con nuestras diez aplicaciones para docentes y las diez aplicaciones para administradores.</p>

               
